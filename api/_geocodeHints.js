function hint(label, lat, lon, kind, aliases = []) {
  const generatedAliases = [
    String(label).replace(/\b\d{4}\b/g, "").replace(/\s+/g, " ").trim(),
    String(label).split(",")[0],
  ].filter(Boolean);
  return { label, lat, lon, kind, aliases: [...new Set([...aliases, ...generatedAliases])] };
}

